    public void atualizaTabela(){
    ResultSet rs;
       
        DefaultTableModel modelo = (DefaultTableModel) tbLivro.getModel();
        modelo.setNumRows(0);
        
                try {
                    rs = AcessoBD.buscarTodosLivros();
                    while(rs.next()){
                        
                        modelo.addRow(new Object[]{
                            rs.getString("titulo"),
                            rs.getString("isbn"),
                            rs.getString("autor"),
                            rs.getString("editora")
                         
                            
                        });
                    }       } catch (SQLException ex) {
                    Logger.getLogger(FrameConsulta.class.getName()).log(Level.SEVERE, null, ex);
                            }
    
    }
    
    
